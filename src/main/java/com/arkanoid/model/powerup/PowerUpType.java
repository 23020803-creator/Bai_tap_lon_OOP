package com.arkanoid.model.powerup;

/**
 * Enum liệt kê các loại PowerUp được hỗ trợ trong game.
 * - EXPAND_PADDLE: Mở rộng kích thước thanh Paddle.
 * - FAST_BALL: Tăng tốc độ của bóng.
 * - EXTRA_LIFE: Thêm mạng chơi.
 */
public enum PowerUpType {
    EXPAND_PADDLE, // PowerUp mở rộng Paddle
    FAST_BALL,     // PowerUp tăng tốc bóng
    EXTRA_LIFE     // PowerUp thêm mạng chơi
}
